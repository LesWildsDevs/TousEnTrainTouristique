const models = require("../models");

// Le trainController sert Ã  faire la liaision avec le AbstractManager et le TrainManager.

const getAll = (req, res) => {
  models.train
    .findAll()
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

// Fonction ajouter pour utiliser la nouvelle fonction getJoin du fichier TrainManager.
const getAllJoin = (req, res) => {
  models.train
    .getJoin()
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const read = (req, res) => {
  models.train
    .find(req.params.id)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.sendStatus(404);
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const edit = (req, res) => {
  const train = req.body;

  // TODO validations (length, format...)

  train.id = parseInt(req.params.id, 10);

  models.train
    .update(train)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const add = (req, res) => {
  const train = req.body;

  // TODO validations (length, format...)

  models.train
    .insert(train)
    .then(([result]) => {
      res.location(`/train/${result.insertId}`).sendStatus(201);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const destroy = (req, res) => {
  models.train
    .delete(req.params.id)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

module.exports = {
  getAll,
  getAllJoin,
  read,
  edit,
  add,
  destroy,
};
